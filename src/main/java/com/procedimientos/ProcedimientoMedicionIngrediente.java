    private void jButtonEliminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonEliminarMouseClicked
        int[] filas = jTable.getSelectedRows();
        if (filas.length == 0) {
            JOptionPane.showMessageDialog(null, "Selecciona una fila para eliminar",
                    "Selección de fila", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        int eleccion = filas.length == 1 ? 
                JOptionPane.showConfirmDialog(null, "¿Desea eliminar la fila seleccionada?",
                    "Eliminar fila",JOptionPane.YES_NO_CANCEL_OPTION):
                JOptionPane.showConfirmDialog(null, "¿Desea eliminar las filas seleccionadas?",
                    "Eliminar filas",JOptionPane.YES_NO_CANCEL_OPTION);
        if (eleccion != JOptionPane.YES_OPTION) {
            return;
        }
        JDBCTableAdapter modelo = (JDBCTableAdapter) jTable.getModel();
        int filas_modificadas = 0;
        String sql = "DELETE FROM ingredient_measurement WHERE measurement_id = ? "
                + "AND ingredient_id = ?;";
        for(int i : filas) {
            try {
                database.empezarTransaccion();
                PreparedStatement statement = database.con.prepareStatement(sql);
                statement.setInt(1, Integer.parseInt(modelo.getValueAt(i, 0).toString()));
                statement.setInt(2, Integer.parseInt(modelo.getValueAt(i, 1).toString()));
                filas_modificadas += statement.executeUpdate();
                database.commit();
            }
            catch (SQLException ex) {
                try {
                    database.rollback();
                } catch (SQLException ex1) {
                    com.error.Error jFrameError = new com.error.Error("Error: " + ex.getMessage());
                    jFrameError.setVisible(true);
                    throw new RuntimeException("Fallo en hacer rollback", ex);
                }
                com.error.Error jFrameError = new com.error.Error("Error: " + ex.getMessage());
                jFrameError.setVisible(true);
                throw new RuntimeException("Fallo en la consulta ejecutada (" + sql + ")", ex);
            }
        }
        if (filas_modificadas == 1) {
            JOptionPane.showMessageDialog(null, "Se eliminó " + filas_modificadas + " fila",
                    "Fila eliminada",JOptionPane.INFORMATION_MESSAGE);
        }
        else {
            JOptionPane.showMessageDialog(null, "Se eliminaron " + filas_modificadas + " filas",
                    "Filas eliminadas",JOptionPane.INFORMATION_MESSAGE);
        }
        jButtonObtenerMouseClicked(null);
    }//GEN-LAST:event_jButtonEliminarMouseClicked

